        <h2>Legal rules</h2>
        
        <p>
            A legal rule, or law, is one which has been officially approved by a state's legislative body. Legal rules are interpreted by courts who decide cases brought before them and may impose sanctions upon those who violate these rules.

Legal rules differ from non-legal rules, such as customs or conventions. Violation of non-legal rules will still often result in consequences for the party concerned, however these rules cannot be enforced by the judiciary like legal rules.

A typical example of a non-legal rule is a political convention in the UK. These conventions develop over time and limit the actions of the executive, the monarch or the parliament in certain ways. However, although they have a very real affect on the behavior of political actors, they cannot be legally enforced in the way that legal rules are. This was unanimously reaffirmed in no uncertain terms by the UK's Supreme Court in R (Miller) v Secretary of State for Exiting the European Union [2017]. In this case the legal enforceability of  the "Sewel Convention" came before the court. The convention is that the UK Parliament at Westminster will normally only make legislation which affects the devolved nations (Scotland, Wales and Northern Ireland) once they have consent from the devolved legislatures. In the context of this case, the devolved legislatures argued that the UK Parliament must receive their blessing before it formally triggers the procedure to withdraw from the European Union. However, the court did not recognise the convention as a legal rule, and therefore held "the policing of its scope and the manner of its operation does not lie within the constitutional remit of the judiciary, which is to protect the rule of law".
